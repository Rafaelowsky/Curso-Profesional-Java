package main;

public class Constantes {
	public static void main(String[] args) {
		
		//Para declarar una constante solamente le agregamos al principio la palabra "final"
		//con esto, no se podra modificar el valor de esa constante en todo el c√≥digo
		
		final double pi = 3.1416, e = 3.14f;
		
		System.out.println(pi);
		System.out.print(e);
	}
}
